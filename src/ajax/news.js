export default {
  getNews (type, keyword, cb) {
    setTimeout(() => {
      switch (type) {
        case 0:
          cb(_news.filter(n => n.title.indexOf(keyword) >= 0 || n.content.indexOf(keyword) >= 0))
          break
        case 1:
          cb(_news.filter(n => n.from.indexOf(keyword) >= 0))
          break
        default:
          cb(_news)
          break
      }
    }, 100)
  }
}
